# Pendekatan Prosedural
def hitung_luas_persegi(sisi):
    return sisi * sisi

def hitung_luas_segitiga(sisi):
    return 4 * sisi

# Pemanggilan fungsi
sisi = 5
luas_persegi = hitung_luas_persegi(sisi)
keliling_segitiga = hitung_luas_segitiga(sisi)

print(f"Luas persegi: {luas_persegi}")
print(f"Keliling segitiga: {keliling_segitiga}")    